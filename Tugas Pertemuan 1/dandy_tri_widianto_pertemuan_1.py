# 1. Mencari nilai KPK dari 3 dan 4

def cari_kpk(a, b):
    kpk = max(a, b)
    while kpk % a != 0 or kpk % b != 0:
        kpk += max(a, b)
    return kpk

hasil = cari_kpk(3, 4)
print(f"KPK dari 3 dan 4 adalah: {hasil}")

# 2. Fungsi untuk menukar posisi dua variabel x dan y

def tukar_buah(piring1, piring2):
    piring3 = piring1
    piring1 = piring2
    piring2 = piring3
    return piring1, piring2

piring1 = "manggis"
piring2 = "pisang"
piring1, piring2 = tukar_buah(piring1, piring2)

