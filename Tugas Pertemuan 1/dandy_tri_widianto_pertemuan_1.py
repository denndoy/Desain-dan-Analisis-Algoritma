# 5. Menghitung volume tabung

import math

def hitung_volume_tabung(jari_jari, tinggi):
    return math.pi * jari_jari ** 2 * tinggi

jari_jari = 3
tinggi = 5
volume = hitung_volume_tabung(jari_jari, tinggi)

print(f"Volume tabung: {volume:.2f}")

# 6. Menghitung volume kerucut

def hitung_volume_kerucut(diameter, tinggi):
    jari_jari = diameter / 2
    return (1/3) * math.pi * jari_jari ** 2 * tinggi

diameter = 5
tinggi = 4
volume = hitung_volume_kerucut(diameter, tinggi)

print(f"Volume kerucut: {volume:.2f}")